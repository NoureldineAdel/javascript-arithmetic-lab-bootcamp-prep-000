function add(a,b) {
  return a + b
}
function subtract(a,b) {
  return a - b
}
function multiply(a,b) {
  return a * b
}
function divide(a,b) {
  return a / b
}
function inc(n){
  incremented = n++
  return incremented
}
function dec(n){
  decremented = n--
  return decremented
}
function makeInt(n){
  return parseInt(n, 10)
}
function preserveDecimal(n){
  return parseFloat(n)
}